const HtmlWebpackPlugin = require("html-webpack-plugin")

module.exports = {
  // 指定构建模式，有两个参数可选
  // 开发阶段使用development,因为打包速度快
  // 上线阶段使用production,因为项目上线需要文件体积小
  mode: 'development',
  entry: "./src/index.js",
  output: {
    path: __dirname + '/dist',
    filename: 'main.js'
  },
  plugins: [
    new HtmlWebpackPlugin()
  ]
}